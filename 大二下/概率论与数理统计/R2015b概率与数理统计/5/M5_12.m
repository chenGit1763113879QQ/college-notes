 clear all;
%定义一个矩阵，输入原始数据
Y=[38 29 36 40;45 42 37 43;58 46 52 41;...
    67 70 65 71;62 64 61 70;58 63 71 69];
Y=Y';                                       %矩阵转置
%将数据矩阵Y转换成8行3列的矩阵，列对应因素A（氮），行对应因素B（磷）
Y=[Y(:,[1,3,5]);Y(:,[2,4,6])];
%定义元胞数组，以元胞数组形式显示转换后的数据
top={'因素','N1','N2','N3'};
left={'P1';'P1';'P1';'P1';'P2';'P2';'P2';'P2'};
disp('显示数据:')
[top;left,num2cell(Y)]                      
%调用anova2的函数作双因素方差分析，返回检验的p值，方差分析表，结构变量
[p,table,stats]=anova2(Y,4,0.01)
c = multcompare(stats)
